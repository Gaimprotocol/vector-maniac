// Map system with 50 unique maps featuring different environments, enemies, and effects
import { GAME_CONFIG } from './constants';

// Terrain type defines how the map is rendered
export type TerrainType = 
  | 'cave'           // Both ceiling and floor (classic)
  | 'surface'        // Only floor, open sky
  | 'ceiling'        // Only ceiling, open bottom (no floor, no civilians/ground enemies)
  | 'openSpace'      // No terrain at all (space)
  | 'city'           // Floor with tall buildings/skyscrapers
  | 'mixed';         // Sections that switch between types

export interface MapTheme {
  id: number;
  name: string;
  environment: 'space' | 'planet' | 'asteroid' | 'nebula' | 'industrial' | 'ice' | 'lava' | 'alien' | 'city' | 'ocean';
  
  // Visual settings
  backgroundColor1: string;
  backgroundColor2: string;
  backgroundColor3: string;
  terrainColor: string;
  terrainMidColor: string;
  terrainGlowColor: string;
  structureColor: string;
  
  // Atmospheric effects
  hasFog: boolean;
  fogColor: string;
  fogDensity: number;
  hasHaze: boolean;
  hazeColor: string;
  hasNebula: boolean;
  nebulaColors: string[];
  hasAurora: boolean;
  auroraColors: string[];
  
  // Terrain settings
  hasTerrain: boolean;  // false = open space (legacy, use terrainType instead)
  terrainType?: TerrainType; // NEW: defines terrain layout - optional for backwards compatibility
  terrainMinHeight: number;
  terrainMaxHeight: number;
  structureChance: number;
  structureTypes: ('building' | 'pipe' | 'tower' | 'crystal' | 'rock' | 'spire' | 'skyscraper' | 'antenna' | 'dome' | 'waterfall' | 'lavafall')[];
  hasHazardousTerrain?: boolean; // Terrain causes damage on collision
  hasFallingDebris?: boolean; // Rocks/debris fall from above
  debrisType?: 'rock' | 'ice' | 'lava' | 'metal'; // Type of falling debris
  
  // Moving obstacles
  hasMovingObstacles?: boolean;
  obstacleType?: 'gates' | 'pistons' | 'lasers' | 'platforms';
  
  // Visual effects
  hasWaterfall?: boolean;
  waterfallColor?: string;
  hasLavaFlow?: boolean;
  lavaFlowColor?: string;
  
  // Enemy configuration
  enemyTypes: string[];
  laserEnemyChance: number;
  enemySpawnRate: number;
  
  // Special features
  hasStars: boolean;
  starDensity: number;
  starColors: string[];
  hasAsteroids: boolean;
  hasPlanetInBackground: boolean;
  planetColor: string;
  planetSize: number;
}

