// Get map by id (1-indexed)
export function getMap(id: number): MapTheme {
  const index = Math.max(0, Math.min(id - 1, MAPS.length - 1));
  return MAPS[index];
}

// Get the effective terrain type for a map (handles legacy maps without terrainType)
export function getTerrainType(map: MapTheme): TerrainType {
  // If terrainType is explicitly set, use it
  if (map.terrainType) {
    return map.terrainType;
  }
  // Legacy fallback: use hasTerrain to determine type
  return map.hasTerrain ? 'cave' : 'openSpace';
}

// Get next map (loops back to 1 after 50)
export function getNextMapId(currentId: number): number {
  return currentId >= MAPS.length ? 1 : currentId + 1;
}

// Check if map requires Ultimate Edition (maps 21-50)
export function isMapLocked(mapId: number, hasUltimate: boolean): boolean {
  return mapId > 20 && !hasUltimate;
}

// The last free map ID (maps 1-20 are free)
export const LAST_FREE_MAP = 20;

// Check if transitioning to next map would start a new wave
export function isNewWave(currentId: number): boolean {
  return currentId >= MAPS.length;
}

